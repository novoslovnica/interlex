export enum AccentParadigm {
    A = 'A', // Баритоническая: ударение всегда на основе (н-р: *rỳba -> ryba, ryby, rybě)
    B = 'B', // Окситоническая: ударение смещено на окончание (н-р: *ženà -> žena, жены, женě)
    C = 'C'  // Мобильная (подвижная): ударение перемещается (н-р: *gòrdъ -> gorod, но косв. падежи: gorodà)
}
